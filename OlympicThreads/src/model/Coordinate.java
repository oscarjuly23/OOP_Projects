package Model;

public class Coordinate {
    private int filas;
    private int columnas;

    public Coordinate(int filas, int columnas) {
        this.filas = filas;
        this.columnas = columnas;
    }

    public int getFilas() {
        return filas;
    }

    public int getColumnas() {
        return columnas;
    }
}
